public class Swithches
{
    public static void main(String[] args)
    {
        //Switch statement
        int day = 7;
        switch(day)
        {
            case 1:
            System.out.println("\nMonday");
            break;
            case 2:
            System.out.println("\nTuesday");
            break;
            case 3:
            System.out.println("\nWednesday");
            break;
            default:
            System.out.println("\nNot Matched");
        }
    }
}